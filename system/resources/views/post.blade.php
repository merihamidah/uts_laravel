         Mata hitam dan kantung mata yang besar memang bikin sebal. Selain mengganggu penampilan, mata panda juga jadi salah satu tanda kalau kamu kurang tidur dan nggak sehat. Stres, terlalu lelah, atau iritasi bisa jadi penyebabnya. Menggunakan concealer kerap jadi pilihan kaum hawa untuk menyamarkan lingkaran hitam di bawah mata. Tapi sebenarnya ada beberapa cara alami yang bisa membantu mengurangi mata panda, seperti misalnya dengan bahan-bahan berikut ini: 1. Essential Oil Kini, essential oil memang sedang menjadi tren dalam dunia perawatan kulit. Kandungannya yang tentu alami dan khasiatnya untuk merawat kulit membuatnya kian digemari. Nah, ternyata, salah satu khasiat dari beberapa essential oil adalah melembapkan dan menutrisi daerah mata, sehingga membantu mencerahkan lingkaran hitam. Hal ini tentunya berkat kandungan vitamin E serta antioksidan yang terdapat di dalam essential oil.
         Beberapa jenis essential oil yang dapat kamu gunakan untuk menyamarkan mata panda adalah Sweet Almond Oil, Argan Oil, Coconut Oil, Grapeseed Oil, serta Rosehip Oil. Caranya, aplikasikan 1-2 tetes essential oil dan pijat secara lembut pada area bawah mata sebelum tidur. 2. Lidah Buaya Bahan alami lainnya yang dapat kamu manfaatkan untuk mencerahkan mata pandaadalah lidah buaya.
         Bahan alami yang satu ini, tentu dapat dengan mudah kamu temui di supermarket. Lidah buaya memiliki kandungan yang mampu melembapkan dan menutrisi, sehingga dapat mencerahkan pigmentasi pada area bawah mata yang gelap dan membuatnya semakin sehat. Untuk menggunakan lidah buaya, kupas kulitnya untuk mendapatkanya gel yang terdapat pada bagian dalam lidah buaya. Kemudian, aplikasikan gel tersebut pada area bawah mata dan berikan pijatan lembut. Diamkan selama 10-15 menit, lalu bersihkan dengan kapas yang dibasahi. 3. Tomat Jika kamu malas mencari essential oil maupun mengupas lidah buaya, bahan yang satu ini bisa menjadi pilihan kamu.
         Bahkan, bahan ini mungkin sudah ada dalam dapur kamu dan dapat langsung kamu gunakan untuk mengurangi lingkaran hitam. Buah tomat yang kerap digunakan untuk bahan masakan ternyata mengandung lycopene yang kaya akan antioksidan dan nutrisi lainnya untuk kulit. Saat kamu mengompres bagian mata dengan irisan tomat, kandungan lycopene tersebut juga dipercaya dapat membantu mengurangi kerutan, lho! Jika kamu ingin memaksimalkan efek mencerahkan dari tomat untuk bagian bawah mata, kamu bisa mencampurkan tomat yang dihaluskan dengan perasan lemon sebagai masker untuk bagian bawah mata. 4. Timun Pasti kamu sudah familiar dengan pemandangan timun yang digunakan sebagai masker mata, bukan? Ternyata, timun memang kerap digunakan sebagai masker mata karena mengandung sederet manfaat untuk mata, lho! Timun mengandung sejumlah antioksidan, terutama vitexin, orientin, dan cucurbitacinsyang membantu merawat area bawah mata yang gelap. Caranya, iris timun secukupnya, kemudian diamkan dalam kulkas selama 30 menit. Setelah 30 menit, ambil 2 iris timun lalu tutup area mata kamu dengan irisan tersebut. Diamkan selama 10 menit lalu bilas wajah dengan air dingin. Selain antioksidan, sejuknya timun juga dapat membantu mengurangi tampilan kantung mata kamu, lho! 5. Kunyit Masih menggunakan bahan yang terdapat di dapur, kunyit memang kerap dimanfaatkan sebagai salah satu bahan untuk masker wajah. Pasalnya, setelah menggunakan masker kunyit, wajah akan langsung terlihat lebih cerah. Nah, begitu pula halnya jika kamu menggunakan kunyit pada area lingkaran hitam. Namun, karena area mata merupakan bagian kulit yang cukup sensitif, akan lebih baik jika kamu mencampurkan 1/2 sendok teh kunyit yang sudah dihaluskan dengan 1 sendok teh essential oil untuk melembapkan kulit. Pijat campuran tersebut secara lembut pada area bawah mata, kemudian diamkan selama 20 menit dan bilas hingga bersih. Cukup mudah, bukan? 
